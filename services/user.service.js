function _delete(_id) {
    var deferred = Q.defer();

    db.users.remove(
        { _id: mongo.helper.toObjectID(_id) },
        function (err) {
            if (err) deferred.reject(err.name + ': ' + err.message);

            deferred.resolve();
        });

    return deferred.promise;
}

function searchByQuery(query) {
	var deferred = Q.defer();
	var set = query;
	
	if (set.name) {
		set.name = {$regex: set.name};
	}
	if (set.username) {
		set.username = {$regex: set.username};
	}
	if (set.phoneNumber) {
		set.phoneNumber = {$regex: set.phoneNumber};
	}
	if (set.email) {
		set.email = {$regex: set.email};
	}
	db.users.find(set).toArray(function (err, result) {
		if (err) deferred.reject(err.name + ': ' + err.message);
		
		if (result) {
			// return users (without hashed password)
			result.forEach(function (user) {
				user =_.omit(user, 'hash');
			});
			deferred.resolve(result);
		} else {
			deferred.resolve();
		}
	});
	
	return deferred.promise;
}